function formatPrice(num) {
    if (num === null || num === undefined || isNaN(num)) return "0";
    let str = num.toString();
    if (!str.includes('.')) return str;
    let [integerPart, decimalPart] = str.split('.');
    let firstSignificantIndex = -1;
    for (let i = 0; i < decimalPart.length; i++) {
        if (decimalPart[i] !== '0') {
            firstSignificantIndex = i;
            break;
        }
    }
    if (firstSignificantIndex === -1) return integerPart;
    let newDecimal = decimalPart.substring(0, firstSignificantIndex + 3);
    newDecimal = newDecimal.replace(/0+$/, '');
    return newDecimal.length > 0 ? `${integerPart}.${newDecimal}` : integerPart;
}

